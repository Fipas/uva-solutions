bool compare(char a, char b) {
	if (toLower(a) == toLower(b)) {
		return a < b;
	} else {
		return toLower(a) < toLower(b);
	}
}

int main() {
	//ios::sync_with_stdio(false);

	int s1, s2;

	cin >> s1 >> s2;

	while (s1 && s2) {
		vector<int> signals;

		signals.PB(s1);
		signals.PB(s2);

		while (cin >> s1 && s1 != 0) {
			signals.PB(s1);
		}

		bool found = false;

		sort(signals.begin(), signals.end());

		for (int i = signals[0]; i <= 18000; i++) {
			int lights_on = 0;

			for (int j = 0; j < signals.size(); j++) {
				if (i % (2 * signals[j]) < (signals[j] - 5))
					lights_on++;
			}

			if (lights_on == signals.size()) {
				found = true;

				//cout << i << endl;

				int seconds = i % 60;
				i /= 60;
				int minutes = i % 60;
				int hours = i / 60;


				printf("%02d:%02d:%02d\n", hours, minutes, seconds);

				break;
			}
		}

		if (!found) {
			cout << "Signals fail to synchronise in 5 hours\n";
		}

		cin >> s1 >> s2;
	}

	cin >> s1;

	return 0;
}
int main() {
	ios::sync_with_stdio(false);

	int n;
	cin >> n;

	while (n--) {

		int days_num;
		int parties;
		cin >> days_num >> parties;

		int days[days_num + 1];
		memset(days, 0, sizeof(days));


		for (int i = 0; i < parties; i++) {
			int interval;
			cin >> interval;

			for (int j = interval; j <= days_num; j += interval)
				days[j] = 1;

		}

		int num_hartals = 0;
		int mod = 6;

		for (int i = 1; i <= days_num; i++) {
			//cout << days[i] << " ";
			if (days[i] && i % mod != 0 && i % (mod + 1) != 0)
				num_hartals++;
			if (i % 7 == 0) {
				mod += 7;
			}
		}

		//cout << endl;

		cout << num_hartals << endl;

	}

	return 0;
}